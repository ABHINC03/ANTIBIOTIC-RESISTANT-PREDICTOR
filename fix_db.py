def clean_seq(s):
    """Remove whitespace, uppercase."""
    return "".join(s.split()).upper()

def validate(gene, seq):
    invalid = set(seq) - set("ATCGN")
    if invalid:
        print(f"  ⚠ {gene}: INVALID chars {invalid}")
        return False
    if len(seq) < 15:
        print(f"  ⚠ {gene}: Too short ({len(seq)} bp)")
        return False
    return True

