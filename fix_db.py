updated = 0
skipped = 0

for db_gene_name, db_seq in all_genes.items():
    key = db_gene_name.lower().strip()

    # ---- Fix blashv-1 ----
    if key == "blashv-1":
        if blashv1_seq and validate("blashv-1", blashv1_seq):
            cursor.execute("UPDATE sequences SET dna_sequence = ? WHERE gene_name = ?",
                           (blashv1_seq, db_gene_name))
            print(f"  ✅ {db_gene_name}: replaced with NCBI blaSHV-1 ({len(blashv1_seq)} bp)")
            updated += 1
        else:
            # Fallback: strip everything before the first ATG in the corrupted value
            raw = db_seq or ""
            match = re.search(r'ATG[ATCGN]+', raw.upper())
            if match:
                candidate = re.sub(r'[^ATCGN]', '', match.group())
                if validate("blashv-1 (recovered)", candidate):
                    cursor.execute("UPDATE sequences SET dna_sequence = ? WHERE gene_name = ?",
                                   (candidate, db_gene_name))
                    print(f"  ⚠ {db_gene_name}: used recovered ATG-stripped sequence ({len(candidate)} bp)")
                    updated += 1
                else:
                    print(f"  ❌ {db_gene_name}: Could not fix — leaving unchanged")
                    skipped += 1
            else:
                print(f"  ❌ {db_gene_name}: No valid ATG found — leaving unchanged")
                skipped += 1
        continue

    # ---- Fix blaTEM-1_variant ----
    if key == "blatem-1_variant":
        if db_seq:
            fixed = clean_seq(db_seq.replace(".", ""))
            if validate("blaTEM-1_variant", fixed):
                cursor.execute("UPDATE sequences SET dna_sequence = ? WHERE gene_name = ?",
                               (fixed, db_gene_name))
                print(f"  ✅ {db_gene_name}: removed '.' character ({len(fixed)} bp)")
                updated += 1
            else:
                print(f"  ❌ {db_gene_name}: still invalid after fix")
                skipped += 1
        continue

    # ---- Populate from browser-collected sequences ----
    if key in SEQUENCES:
        new_seq = clean_seq(SEQUENCES[key])
        if validate(key, new_seq):
            cursor.execute("UPDATE sequences SET dna_sequence = ? WHERE gene_name = ?",
                           (new_seq, db_gene_name))
            print(f"  ✅ {db_gene_name}: {len(new_seq)} bp")
            updated += 1
        else:
            skipped += 1
        continue

    # ---- SNP marker genes — flag only, do not touch ----
    if key in ("blatemp_c32t", "nfsa_q113ter", "uhpt_e350q"):
        print(f"  ℹ {db_gene_name}: SNP marker — requires literature sequence (skipped)")
        skipped += 1

conn.commit()
