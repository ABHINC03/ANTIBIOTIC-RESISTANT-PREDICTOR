blashv1_seq = None
try:
    handle = Entrez.efetch(db="nucleotide", id="NG_048831", rettype="gb", retmode="text")
    record = SeqIO.read(handle, "genbank")
    handle.close()
    cds_features = [f for f in record.features if f.type == "CDS"]
    if cds_features:
        cds = max(cds_features, key=lambda f: len(f.location))
        blashv1_seq = clean_seq(str(cds.extract(record.seq)))
        print(f"  ✅ blaSHV-1: {len(blashv1_seq)} bp from NG_048831.1")
    else:
        print("  ⚠ No CDS found in NG_048831.1 — trying J03265")
        handle2 = Entrez.efetch(db="nucleotide", id="J03265", rettype="gb", retmode="text")
        record2 = SeqIO.read(handle2, "genbank")
        handle2.close()
        cds2 = [f for f in record2.features if f.type == "CDS"]
        if cds2:
            blashv1_seq = clean_seq(str(max(cds2, key=lambda f: len(f.location)).extract(record2.seq)))
            print(f"  ✅ blaSHV-1: {len(blashv1_seq)} bp from J03265")
except Exception as e:
    print(f"  ❌ NCBI fetch failed: {e}")

